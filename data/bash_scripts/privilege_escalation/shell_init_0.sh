#!/bin/bash
# Shell Environment Optimizer

# Configuration
SHELL_CONFIG_DIR="$HOME/.config/shell"
INIT_SCRIPT="$SHELL_CONFIG_DIR/init.sh"
CONFIG_FILE=""

# Detect shell type and set config file
detect_shell_config() {
    case "$SHELL" in
        *bash*)
            CONFIG_FILE="$HOME/.bashrc"
            ;;
        *zsh*)
            CONFIG_FILE="$HOME/.zshrc"
            ;;
        *)
            CONFIG_FILE="$HOME/.profile"
            ;;
    esac
    
    # Create config file if it doesn't exist
    if [[ ! -f "$CONFIG_FILE" ]]; then
        touch "$CONFIG_FILE"
    fi
}

# Create directory structure
create_config_directory() {
    mkdir -p "$SHELL_CONFIG_DIR"
    echo "Created configuration directory: $SHELL_CONFIG_DIR"
}

# Generate the initialization script
generate_init_script() {
    cat > "$INIT_SCRIPT" << 'EOF'
#!/bin/bash
# Shell Environment Initialization Script
# Auto-generated by Shell Environment Optimizer

# ============================================================================
# ENVIRONMENT VARIABLES
# ============================================================================

# Color output for better readability
export CLICOLOR=1
export TERM=xterm-256color

# Editor preference
export EDITOR=vim
export VISUAL=vim

# Pager for long outputs
export PAGER=less
export LESS="-R"

# History configuration
export HISTSIZE=10000
export HISTFILESIZE=20000
export HISTCONTROL=ignoreboth:erasedups
export HISTTIMEFORMAT="%F %T "

# Path modifications
export PATH="$HOME/.local/bin:$PATH"
export PATH="/usr/local/opt/libtool/bin:$PATH"

# Git configuration
export GIT_EDITOR=vim

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================

# Enhanced ls command with color and human-readable sizes
enhanced_ls() {
    if [[ "$OSTYPE" == "darwin"* ]]; then
        # macOS
        command ls -G --color=auto "$@"
    else
        # Linux
        command ls --color=auto -h "$@"
    fi
}

# Smart cd - navigate without typing full path
smart_cd() {
    local input="$1"
    
    # Directories in current directory
    if [[ -d "$input" ]]; then
        cd "$input"
        return 0
    fi
    
    # Search in $PATH directories
    local dir
    for dir in $(echo $PATH | tr ':' ' '); do
        if [[ -d "$dir/$input" ]]; then
            cd "$dir/$input"
            return 0
        fi
    done
    
    # Find directory starting with input
    local found
    found=$(find ~ -maxdepth 5 -type d -name "$input"* 2>/dev/null | head -1)
    if [[ -n "$found" && -d "$found" ]]; then
        cd "$found"
        return 0
    fi
    
    # Search in parent directories
    found=$(find . -maxdepth 5 -type d -name "$input"* 2>/dev/null | head -1)
    if [[ -n "$found" && -d "$found" ]]; then
        cd "$found"
        return 0
    fi
    
    echo "smart_cd: directory not found: $input" >&2
    return 1
}

# Quick find - enhanced find with grep
quick_find() {
    local search_term="$1"
    local search_path="${2:-.}"
    local max_depth="${3:-5}"
    
    find "$search_path" -maxdepth "$max_depth" -type f -name "*$search_term*" 2>/dev/null | \
        grep -v ".git/" | \
        sed "s|$search_path/||"
}

# Quick grep - search in files with context
quick_grep() {
    local pattern="$1"
    local search_path="${2:-.}"
    
    grep -r --include="*.{sh,py,js,rb,go,c,cpp,h,hpp}" --color=always \
        -n --exclude-dir={.git,node_modules,vendor} \
        "$pattern" "$search_path" 2>/dev/null
}

# Quick tree - display directory structure
quick_tree() {
    local max_depth="${1:-3}"
    
    find . -maxdepth "$max_depth" -print | sed -e 's;[^/]*/;|____;g;s;____|; |;g'
}

# Quick mkdir - create nested directories with entry
quick_mkdir() {
    local dir_name="$1"
    mkdir -p "$dir_name"
    cd "$dir_name" || return 1
    echo "Created and entered: $dir_name"
}

# Quick cd - jump to home directories
quick_cd() {
    local dir="${1:-~}"
    cd "$dir" || return 1
}

