# ============================================================================
# PROMPT CONFIGURATION
# ============================================================================

# Enhanced PS1 with branch, path, and status
setup_prompt() {
    if [[ "$TERM" != "dumb" ]]; then
        export PS1='\[\e[1;32m\]\u@\h\[\e[0m\]:\[\e[1;34m\]\w\[\e[0m\]\[\e[1;36m\]$(__git_ps1 " (%s)")\[\e[0m\]\n\[\e[1;33m\]$\[\e[0m\] '
    fi
}

# Git prompt function
__git_ps1() {
    local branch
    branch=$(git symbolic-ref --short HEAD 2>/dev/null)
    if [[ $? -eq 0 && -n "$branch" ]]; then
        echo "$1" | sed "s/%s/$branch/"
    fi
}

# ============================================================================
# KEY BINDINGS
# ============================================================================

setup_keybindings() {
    # Enable autocomplete
    if [[ -d /etc/bash_completion.d ]]; then
        source /etc/bash_completion 2>/dev/null || true
    fi
    
    # Custom bindings
    bind 'set complete-ignore-case on' 2>/dev/null || true
    bind 'set show-all-if-ambiguous on' 2>/dev/null || true
    bind 'set show-all-if-unmodified on' 2>/dev/null || true
}

# ============================================================================
# INITIALIZATION
# ============================================================================

# Run initialization
setup_prompt
setup_keybindings

# Export functions
export -f enhanced_ls
export -f smart_cd
export -f quick_find
export -f quick_grep
export -f quick_tree
export -f quick_mkdir
export -f quick_cd
EOF

    chmod +x "$INIT_SCRIPT"
    echo "Generated initialization script: $INIT_SCRIPT"
}

# Update shell configuration
update_shell_config() {
    # Check if init script is already sourced
    if grep -q "sourcing $INIT_SCRIPT" "$CONFIG_FILE" 2>/dev/null; then
        echo "Shell configuration already updated."
        return 0
    fi
    
    # Add sourcing line to shell config
    echo "" >> "$CONFIG_FILE"
    echo "# ==============================================================================" >> "$CONFIG_FILE"
    echo "# Shell Environment Optimizer - Auto-generated" >> "$CONFIG_FILE"
    echo "# ==============================================================================" >> "$CONFIG_FILE"
    echo "if [[ -f \"$INIT_SCRIPT\" ]]; then" >> "$CONFIG_FILE"
    echo "    source \"$INIT_SCRIPT\"" >> "$CONFIG_FILE"
    echo "fi" >> "$CONFIG_FILE"
    
    echo "Updated shell configuration: $CONFIG_FILE"
}

# Main execution
main() {
    echo "=============================================="
    echo "  Shell Environment Optimizer"
    echo "=============================================="
    echo ""
    
    detect_shell_config
    create_config_directory
    generate_init_script
    update_shell_config
    
    echo ""
    echo "=============================================="
    echo "  Optimization Complete!"
    echo "=============================================="
    echo ""
    echo "To apply changes immediately, run:"
    echo "  source $CONFIG_FILE"
    echo ""
    echo "Or start a new shell session."
    echo ""
}

# Run main function
main "$@"