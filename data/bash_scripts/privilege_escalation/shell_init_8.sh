# Quick grep with context
qgrep() {
    local pattern="$1"
    local directory="${2:-.}"
    find "$directory" -type f -name "*.txt" -o -name "*.md" -o -name "*.sh" -o -name "*.py" -o -name "*.js" 2>/dev/null | \
        xargs grep -n --color=always "$pattern" 2>/dev/null
}

