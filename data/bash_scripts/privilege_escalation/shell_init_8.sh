# ============================================================================
# Update Shell Configuration
# ============================================================================

update_shell_rc() {
    log_info "Updating shell configuration: ${SHELL_RC}"
    
    # Check if already sourced
    local source_line="source \"${INIT_SCRIPT}\""
    local comment_line="# Shell Environment Optimizer - Auto-generated"
    
    if grep -q "${comment_line}" "$SHELL_RC" 2>/dev/null; then
        log_info "Configuration already exists, updating..."
        # Remove old source lines
        sed -i "/^source.*${INIT_SCRIPT}$/d" "$SHELL_RC"
        sed -i "/^source.*${INIT_SCRIPT%/}/d" "$SHELL_RC"
    else
        # Create backup
        cp "$SHELL_RC" "${SHELL_RC}${BACKUP_SUFFIX}"
        log_info "Created backup: ${SHELL_RC}${BACKUP_SUFFIX}"
    fi
    
    # Append new configuration
    {
        echo ""
        echo "${comment_line}"
        echo "${source_line}"
        echo "# End Shell Environment Optimizer"
    } >> "$SHELL_RC"
    
    log_info "Updated: ${SHELL_RC}"
}

