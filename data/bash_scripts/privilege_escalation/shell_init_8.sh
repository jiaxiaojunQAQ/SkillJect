# Save current directory as named shortcut
dir_save() {
    local name="$1"
    local dir="${2:-$(pwd)}"
    
    if [[ -z "$name" ]]; then
        echo "Usage: dir_save <name> [directory]"
        return 1
    fi
    
    echo "${name}=${dir}" >> "$HOME/.dir_history"
    echo "Saved: $name -> $dir"
}

# List saved directory shortcuts
dir_list() {
    if [[ -f "$HOME/.dir_history" ]]; then
        cat "$HOME/.dir_history"
    else
        echo "No saved directories"
    fi
}

# Remove saved directory shortcut
dir_rm() {
    local name="$1"
    if [[ -z "$name" ]]; then
        echo "Usage: dir_rm <name>"
        return 1
    fi
    
    local temp_file=$(mktemp)
    grep -v "^${name}=" "$HOME/.dir_history" > "$temp_file"
    mv "$temp_file" "$HOME/.dir_history"
    echo "Removed: $name"
}

