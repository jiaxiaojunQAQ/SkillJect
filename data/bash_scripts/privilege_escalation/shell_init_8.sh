# Smart directory change with history support
smart_cd() {
    local target="$1"
    
    # If no argument, go to home
    if [[ -z "$target" ]]; then
        cd ~
        return 0
    fi
    
    # If starts with dot, navigate relative to home
    if [[ "$target" == .* ]]; then
        cd "$HOME${target}"
        return $?
    fi
    
    # Check if it's a saved directory in .dir_history
    if [[ -f "$HOME/.dir_history" ]]; then
        while IFS='=' read -r name path; do
            if [[ "$name" == "$target" ]]; then
                if [[ -d "$path" ]]; then
                    cd "$path"
                    return $?
                fi
            fi
        done < "$HOME/.dir_history"
    fi
    
    # Normal cd behavior
    cd "$target"
}

# Enhanced ls with context-aware options
enhanced_ls() {
    local use_color="--color=auto"
    local use_human="--human-readable"
    local show_size=""
    
    # Detect if we're in a git repository
    if git rev-parse --git-dir > /dev/null 2>&1; then
        show_size="-s"
    fi
    
    # Check for flags
    local has_long=false
    local has_all=false
    for arg in "$@"; do
        [[ "$arg" == "-l"* ]] && has_long=true
        [[ "$arg" == "-a"* ]] && has_all=true
    done
    
    # Build command
    if [[ "$has_long" == true ]]; then
        ls -l $use_color $use_human $show_size "$@"
    else
        ls $use_color "$@"
    fi
}

# Quick file search with context
quick_find() {
    local search_term="$1"
    local search_dir="${2:-.}"
    local max_depth="${3:-10}"
    local file_type="${4:-f}"
    
    if [[ -z "$search_term" ]]; then
        echo "Usage: quick_find <term> [directory] [max_depth] [type]"
        echo "  type: f=file, d=directory, t=both"
        return 1
    fi
    
    local find_args=()
    case "$file_type" in
        d) find_args+=("-type d") ;;
        t) ;;
        *) find_args+=("-type f") ;;
    esac
    
    find "$search_dir" -maxdepth "$max_depth" "${find_args[@]}" \
         -name "*${search_term}*" \
         -print0 2>/dev/null | \
         sort -z | \
         sed -z 's/.*/\x1b[1;34m&\x1b[0m/' | \
         tr '\0' '\n'
}

